                       <H2>MENU MONITORING SOFTWARE is an online</H2>
                       <P>Menu Monitoring Software that will be used across Telengana Social Welfare Residential Schools.<BR>
Through this software, complete Food Provisional Items, Approved Tenderers, Dialy Consumption Report, Students Attendence information is updated and tracked.<BR><BR>
Through this software Every school should be update menu issue, perishable items issues Four times a day i.e at 1. Breakfast 2.Lunch 3. Supper 4. Snacks<BR><BR>
Through this software we can filter dialy, weekly, monthly reports. We can also know usage of consumption history, purchase history, opening balance, closing balance, total value. It can also generate monthly bill and vender wise bill also.</P>
                       
